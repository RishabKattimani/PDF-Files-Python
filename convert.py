#------------------------------------------------------------------------------
# Imports
from fpdf import FPDF

#------------------------------------------------------------------------------
# Setup
pdf = FPDF()
pdf.add_page()
pdf.set_font("Arial", size = 15)
f = open("mytext.txt", "r")

#------------------------------------------------------------------------------
# Converting The Text
for x in f:
	pdf.cell(200, 10, txt = x, ln = 1, align = 'C')
pdf.output("mypdf.pdf")

